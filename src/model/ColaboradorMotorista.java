package model;

public class ColaboradorMotorista {
    String motorista;
    String veiculo;

    public ColaboradorMotorista(String motorista, String veiculo) {
        this.motorista = motorista;
        this.veiculo = veiculo;
    }


    public String getMotorista() {
        return motorista;
    }

    public String getVeiculo() {
        return veiculo;
    }
}
